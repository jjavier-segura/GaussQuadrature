function [xz,wns,w,uv]=GHa(n,expoc)
% Gauss-Hermite quadrature. Asymptotic method.
% Copyright 2025 A. Gil, J. Segura, N. M. Temme
%
% [x,w,ws,v]=GHa(n,expoc)
%
% INPUT:
%   n: degree 
%   expoc (optional): cut off (only weights such that w/wmax<10^(-expoc) are computed)
%
% OUTPUT:
%   x: nodes
%   w: unscaled weights 
%   ws: scaled weights
%   v: barycentric weights
%
%
if nargin<2 
  expoc=1.e300;
end
expoc=max(expoc,20);
[xce,wge,wse]=hermweightnodesElem(n);
[xcA,wgA,wsA]=hermweightnodesAiry(n);
nz=floor(n/2);
na=max(20,floor(0.1*n));
if mod(n,2)>0
  nz=nz+1;
end 
for i=1:nz-na
  xc(i)=xce(i); 
  wg(i)=wge(i);
  ws(i)=wse(i);
end    
j=0;
for i=nz-na+1:nz
  j=j+1;  
  xc(i)=xcA(j); 
  wg(i)=wgA(j);
  ws(i)=wsA(j);
end    
xcut=sqrt(2.3*expoc);
xn=xc(1);
k=0;
while xn<xcut && k<nz
  k=k+1;
  xz(k)=xn;
  wns(k)=wg(k);
  w(k)=ws(k);
  if k<nz
    xn=xc(k+1);
  end
end
ind=n-2*floor(n/2);
xz=[-xz(end:-1:1+ind) xz];
wns=[wns(end:-1:1+ind) wns];
w=[w(end:-1:1+ind) w];
uv=-cumprod(-ones(1,length(xz))).*sqrt(wns);
end
