function [x,w]=legen(n)
if n>80
 flo=floor(n/2);
 ind=n-2*flo;
 [bessx,bessw]=bessB(n);
 [elemx,elemw]=elemO(n);
 x=[bessx elemx];
 w=[bessw elemw];
 l=length(w);
 if ind==1
    w=[w w(l-1:-1:1)];
    x=[x -x(l-1:-1:1)];
 else
    w=[w w(l:-1:1)];
    x=[x -x(l:-1:1)]; 
 end 
else
  [x,w]=tableleg(n);
end    
end 

function [xk,wk]=elemO(n)
flo=floor(n/2);
nli=36;   
ind=n-2*flo;
flo2=floor(flo/2);
lim=max(nli+4,flo-flo2);
ck=-cos(pi*(4*(nli:lim)-1)/(4*n+2));
ck2=sin(pi*(2*(lim+1:flo+ind)-n-1)/(2*n+1));
ck=[ck ck2];
sk=sin(pi*(4*(nli:flo+ind)-1)/(4*n+2));
ctk=ck./sk;
ctk2=ctk.*ctk;
ctk4=ctk2.*ctk2;
ctk6=ctk4.*ctk2;
n12=2*n+1;
n122=n12*n12;
n124=n122*n122;
n126=n124*n122;
f1=1 - 0.5/n122 + 11/8/n124 - 173/16/n126;
fk=f1+(7/6/n124 -103/4/n126)*ctk2 -15.1*ctk4/n126;
xk=ck.*fk;
gk=f1+(7/2/n124 -309/4/n126)*ctk2+(7/3/n124-127/n126)*ctk4-302/5/n126*ctk6;
wk=2*pi/n12*sk.*gk;
end


