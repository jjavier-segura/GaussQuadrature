% FAST Gauss-Legendre quarature
% Asymptotic formulas for Gauss-Legendre (n>80)
% and look-up table for n<=80
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% n: degrre
% x: nodes
% w: weights
